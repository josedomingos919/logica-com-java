public class OperadoresAritmetricos {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        double n1, n2, total;
        
        n1 = 8;
        n2 = 3;
        
        total = n1 + n2;
        
        System.out.println("Total +: " + total);
        
        total = n1 * n2;
        
        System.out.println("Total *:  " + total);
        
        
        total = n1 % n2;
        
        System.out.println("Resto *:  " + total);
    }
    
}
